package com.Loops;
import java.util.Scanner;
/**
 * Print 1 to 10 program
 * @author Prajwal
 * @since 31/05/2023
 * @version jdk11
 */

public class Print1To10 {

	public static void main(String[] args) {
      int count=1;
      
      while (count<11)
      {  
    	   System.out.println("count is: "+count);
    	   count++;
      }
	}

}
