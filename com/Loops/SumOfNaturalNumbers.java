package com.Loops;
import java.util.Scanner;
/**
 * Sum of Natural Numbers program
 * @author Prajwal
 * @since 31/05/2023
 * @version jdk11
 */
public class SumOfNaturalNumbers {

	public static void main(String[] args) {
		Scanner N = new Scanner(System.in);
		int sum=0;
		int num = N.nextInt();
		for(int i = 1; i <= num; i++);
		  sum=sum+1;
   }
      
}
